    private void displayAreaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_displayAreaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_SHIFT && octave){
            if (octaveDif == 0 && cnt == endNote+1){
                displayArea.setEnabled(false);
                SaveInFile();
            }
            
            if (cnt == letters.length){
                cnt = 0;
                octaveDif--;
                System.out.println("octave dif " + octaveDif);
            }
            
            displayArea.append("\n");
            displayArea.append("Input key for " + letters[cnt] + ": ");
            cnt++;
        }
        if (evt.getKeyCode() == KeyEvent.VK_CONTROL && !octave){
            GetOctave();
        }
    }//GEN-LAST:event_displayAreaKeyPressed

