    private void SaveInFile(){
        try{
            path = System.getProperty("user.dir") + "\\UserPreferences\\" + fileName.getText() + ".txt";
            f = new File (path);
            
            fw = new FileWriter(f, true);
            bw = new BufferedWriter (fw);
            
            displayArea.setCaretPosition(0);
            String[] lines = displayArea.getText().split("\\n");
            int linesIndex = 0;
            int arrStart;
            String in = "";
            int note1 = begNote;
            
            for (octaveDif = octave2 - octave1; octaveDif>=0; octaveDif--){
                for (arrStart = note1; arrStart <= 11; arrStart ++){
                    int temp = lines[linesIndex].lastIndexOf(":");
                    in = lines[linesIndex].substring(temp + 1);
                    System.out.println(in);
                    bw.append(letters[arrStart] + " " + in.trim());
                    bw.newLine();
                    linesIndex++;
                    if (octaveDif == 0 && arrStart == endNote){
                        break;
                    }
                }
                note1 = 0;
            }
            bw.flush();
            bw.close();
            
            Home h = new Home();
            h.begNote = begNote;
            h.endNote = endNote;
            this.setVisible(false);
            h.setVisible(true);
        }
        catch (IOException e){
            System.out.println(e);
        }
    }
    
    public void GetOctave(){
        int index;
        String oct = "";
        try{
            path = System.getProperty("user.dir") + "\\UserPreferences\\" + fileName.getText() + ".txt";
            f = new File (path);
            f.createNewFile();
            
            if (!octaveCheck){
                fw = new FileWriter(f);
            }
            else{
                fw = new FileWriter(f, true);
            }
            bw = new BufferedWriter (fw);
            
            displayArea.setCaretPosition(0);
            String line = displayArea.getText();
            index = line.lastIndexOf(":");
            oct = line.substring(index + 1).trim();
            bw.write("Octave" + oct);
            bw.newLine();
            bw.flush();
            bw.close();
        }
        catch (IOException e){
            System.out.println(e);
        }
        
        if (!octaveCheck){
            displayArea.setText("");
            displayArea.setText("Enter octave number of the last key, and then press Control: ");
            octave1 = Integer.parseInt(oct);
            octaveCheck = true;
        }
        else{
            octave = true;
            octave2 = Integer.parseInt(oct);
            octaveDif = octave2 - octave1;
            System.out.println(octaveDif);
            
            displayArea.setText("");
            instructions.setText("Enter the key that correspons to the note displayed. After every key entered, press Shift.");
            displayArea.append("Input key for " + letters[cnt] + ": ");
            cnt++;
        }
    }
    
